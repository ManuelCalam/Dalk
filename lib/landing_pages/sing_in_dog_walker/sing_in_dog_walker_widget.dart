Future<void> _startVerification() async {
  debugPrint('üöÄ INICIANDO VERIFICACI√ìN CON VERIFICAMEX');

  String? userUuid;
  String? userEmail;

  try {
    // ‚úÖ VALIDAR CAMPOS
    if (!validarCamposObligatorios()) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Por favor completa todos los campos')),
      );
      return;
    }

    // ‚úÖ CREAR USUARIO EN AUTH
    debugPrint('üë§ Creando usuario en Supabase Auth...');
    final user = await authManager.createAccountWithEmail(
      context,
      _model.emailDogWalkerInputTextController.text,
      _model.passDogWalkerInputTextController.text,
    );

    if (user == null) {
      throw Exception('No se pudo crear el usuario en Supabase Auth');
    }

    // üîê INICIAR SESI√ìN PARA OBTENER TOKEN
    final signIn = await Supabase.instance.client.auth.signInWithPassword(
      email: _model.emailDogWalkerInputTextController.text,
      password: _model.passDogWalkerInputTextController.text,
    );

    final session = signIn.session ?? Supabase.instance.client.auth.currentSession;
    if (session == null) {
      throw Exception('No se pudo obtener la sesi√≥n del usuario');
    }

    final accessToken = session.accessToken;
    userUuid = session.user.id;
    userEmail = session.user.email;

    debugPrint('‚úÖ Usuario autenticado correctamente');
    debugPrint('UUID: $userUuid | Email: $userEmail');

    // ‚úÖ INSERTAR USUARIO EN TABLA "users"
    await Supabase.instance.client.from('users').insert({
      'uuid': userUuid,
      'name': _model.nameDogWalkerInputTextController.text,
      'email': userEmail,
      'phone': _model.phoneDogWalkerInputTextController.text,
      'birthdate': supaSerialize<DateTime>(_model.datePicked),
      'gender': _model.genderDogWalkerMenuValue,
      'address': _model.streetDogWalkerInputTextController.text,
      'houseNumber': _model.interiorNumberDogWalkerInputTextController.text,
      'zipCode': _model.zipCodeDogWalkerInputTextController.text,
      'neighborhood': _model.neighborhoodDogWalkerInputTextController.text,
      'city': _model.cityDogWalkerInputTextController.text,
      'usertype': 'Paseador',
      'verification_status': 'pending_verification',
    });

    debugPrint('‚úÖ Usuario guardado en tabla "users"');

    // ‚úÖ INSERTAR DIRECCI√ìN
    await Supabase.instance.client.from('addresses').insert({
      'uuid': userUuid,
      'alias': 'Mi Direcci√≥n',
      'address': _model.streetDogWalkerInputTextController.text,
      'houseNumber': _model.interiorNumberDogWalkerInputTextController.text,
      'zipCode': _model.zipCodeDogWalkerInputTextController.text,
      'neighborhood': _model.neighborhoodDogWalkerInputTextController.text,
      'city': _model.cityDogWalkerInputTextController.text,
    });

    debugPrint('‚úÖ Direcci√≥n guardada');

    // ‚úÖ LLAMAR EDGE FUNCTION create_verification
    debugPrint('üì° Llamando a Edge Function create_verification...');
    final response = await Supabase.instance.client.functions.invoke(
      'create_verification',
      body: {
        'action': 'create_session',
        'user_id': userUuid,
        'email': userEmail,
        'access_token': accessToken,
      },
    );

    debugPrint('üìä Status: ${response.status}');
    debugPrint('üìä Data: ${jsonEncode(response.data)}');

    if (response.status != 200) {
      throw Exception('Error en create_verification: ${response.data}');
    }

    final data = response.data is String
        ? jsonDecode(response.data)
        : response.data as Map<String, dynamic>;

    final formUrl = data['form_url'] ?? '';
    final sessionId = data['session_id'] ?? '';
    final tokenToPass = data['access_token'] ?? accessToken;

    if (formUrl.isEmpty || sessionId.isEmpty) {
      throw Exception('No se recibi√≥ form_url o session_id v√°lidos');
    }

    debugPrint('‚úÖ formUrl recibido: $formUrl');

    // ‚úÖ NAVEGAR AL WEBVIEW
    if (!mounted) return;

    await Navigator.push(
      context,
      MaterialPageRoute(
        builder: (_) => IneValidationWebviewWidget(
          formUrl: formUrl,
          sessionId: sessionId,
          accessToken: tokenToPass,
        ),
      ),
    );

    debugPrint('üåê WebView abierto correctamente');

  } catch (e) {
    debugPrint('üí• Error en _startVerification: $e');

    // üßπ LIMPIEZA SI FALLA
    if (userUuid != null) {
      try {
        await Supabase.instance.client.from('addresses').delete().eq('uuid', userUuid);
        await Supabase.instance.client.from('users').delete().eq('uuid', userUuid);
        await authManager.signOut();
        debugPrint('üßπ Usuario eliminado correctamente');
      } catch (deleteError) {
        debugPrint('‚ùå Error eliminando usuario: $deleteError');
      }
    }

    if (mounted) {
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('Error durante la verificaci√≥n: $e'),
          backgroundColor: Colors.red,
        ),
      );
    }
  }
}

