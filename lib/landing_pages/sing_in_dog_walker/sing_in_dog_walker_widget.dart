Future<bool> _requestCameraPermission() async {
  debugPrint('üì∑ Solicitando permisos de c√°mara...');
  
  try {
    final status = await Permission.camera.request();
    
    debugPrint('üì∑ Estado del permiso: $status');
    
    if (status == PermissionStatus.granted) {
      debugPrint('‚úÖ Permisos de c√°mara concedidos');
      return true;
    } else if (status == PermissionStatus.denied) {
      debugPrint('‚ùå Permisos de c√°mara denegados');
      return false;
    } else if (status == PermissionStatus.permanentlyDenied) {
      debugPrint('‚ùå Permisos de c√°mara permanentemente denegados');
      
      await _showPermissionSettingsDialog();
      return false;
    }
    
    return false;
  } catch (e) {
    debugPrint('üí• Error solicitando permisos: $e');
    return false;
  }
}

